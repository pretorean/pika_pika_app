class PostMessage {
  final String id;
  final String body;
  final String title;
  final String userId;
  final String firstName;
  final String lastName;
  final int likes;
  final int views;
  final String type;
  final String createDate;
  final String modifyDate;

  PostMessage({
    this.id,
    this.body,
    this.title,
    this.userId,
    this.firstName,
    this.lastName,
    this.likes,
    this.views,
    this.type,
    this.createDate,
    this.modifyDate,
  });
}
